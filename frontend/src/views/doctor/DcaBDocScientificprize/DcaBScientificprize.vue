<template>
  <a-card title="进院工作以来科研获奖情况">
    <div>
      <a-button
        @click="handleAdd"
        type="primary"
        :loading="loading"
      >添加行</a-button>
      <a-button
        @click="handleDelete"
        type="primary"
        :loading="loading"
      >删除行</a-button>
    </div>
    <a-table
      :columns="columns"
      :data-source="dataSource"
      :rowKey="record => record.id"
      :rowSelection="{selectedRowKeys: selectedRowKeys, onChange: onSelectChange}"
      bordered
      :scroll="scroll"
    >
     
      <template
        slot="spProjectName"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
          <a-textarea
            @blur="e => inputChange(e.target.value,record,'spProjectName')"
            :value="record.spProjectName"
          >
          </a-textarea>
        </div>
      </template>
      <template
        slot="srProjectGrade"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
          <a-select
            :value="record.srProjectGrade"
            style="width: 100%"
            @change="(e,f) => handleSelectChange(e,f,record,'srProjectGrade')"
          >
            <a-select-option value="国家级">
              国家级
            </a-select-option>
            <a-select-option value="省部级">
              省部级
            </a-select-option>
            <a-select-option value="校级">
              校级
            </a-select-option>
            <a-select-option value="其他">
              其他
            </a-select-option>
          </a-select>
        </div>
      </template>
      <template
        slot="srProjectLevel"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
           <a-select
            :value="record.srProjectLevel"
            style="width: 100%"
            @change="(e,f) => handleSelectChange(e,f,record,'srProjectLevel')"
          >
            <a-select-option value="一等奖">
              一等奖
            </a-select-option>
            <a-select-option value="二等奖">
              二等奖
            </a-select-option>
            <a-select-option value="三等奖">
              三等奖
            </a-select-option>
             <a-select-option value="其他">
              其他
            </a-select-option>
          </a-select>
        </div>
      </template>
      <template
        slot="srPrizeDept"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
          <a-textarea
            @blur="e => inputChange(e.target.value,record,'srPrizeDept')"
            :value="record.srPrizeDept"
          >
          </a-textarea>
        </div>
      </template>
      <template
        slot="srPrizeDate"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text==""|| text==null?"":text.substr(0,10)}}
        </div>
        <div v-else>
          <a-date-picker
            :defaultValue="(text=='' || text==null)?'':moment(text, dateFormat)"
            @change="(e,f) => handleChange(e,f,record,'srPrizeDate')"
          />
        </div>
      </template>
      <template
        slot="srPrizeRanknum"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
          <a-input-number
            @blur="e => inputChange(e.target.value,record,'srPrizeRanknum')"
            :value="record.srPrizeRanknum"
            :precision="0"
          >
          </a-input-number>
        </div>
      </template>
      <template
        slot="daoshiRanknum"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          {{text}}
        </div>
        <div v-else>
          <a-input-number
            @blur="e => inputChange(e.target.value,record,'daoshiRanknum')"
            :value="record.daoshiRanknum"
            :precision="0"
          >
          </a-input-number>
        </div>
      </template>
      <template
        slot="isUse"
        slot-scope="text, record"
      >
        <a-checkbox
          @change="e => onIsUseChange(e,record,'isUse')"
          :checked="text"
        ></a-checkbox>
      </template>
      <template
        slot="fileId"
        slot-scope="text, record"
      >
        <div v-if="record.state==3 || record.state==1">
          <a
            :href="record.fileUrl"
            v-if="text!=null && text !=''"
            target="_blank"
          >查看</a>
        </div>
        <div v-else>
          <a-button
            type="dashed"
            block
            @click="OpenFile(record)"
          >
           {{record.fileId!=null &&record.fileId !=''?'已上传':'上传' }}
          </a-button>
        </div>
      </template>
    </a-table>
    <div>
      <a-button
        @click="handleSave"
        type="primary"
        :loading="loading"
      >保存草稿</a-button>
      <a-button
        @click="handleSubmit"
        type="primary"
        :loading="loading"
      >提交</a-button>
    </div>
    <tableUpload-file
      ref="upFile"
      :fileId="editRecord.fileId"
      :fileVisiable="fileVisiable"
      @setFileId="setFileId"
    >
    </tableUpload-file>
  </a-card>
</template>

<script>
import moment from 'moment';
import TableUploadFile from '../../common/TableUploadFile'
export default {
  data () {
    return {
      dateFormat: 'YYYY-MM-DD',
      dataSource: [],
      selectedRowKeys: [],
      loading: false,
      CustomVisiable: false,
      idNums: 10000,
      fileVisiable: false,
      editRecord: {
        fileId: ''
      },
       scroll: {
        x: 1600,
        y: window.innerHeight - 200 - 100 - 20 - 80
      },
    }
  },
  components: { TableUploadFile },
  mounted () {
    this.fetch()
  },
  methods: {
    moment,
    showFile (record) {
      window.location.href = record.fileUrl
    },
    OpenFile (record) {
      this.editRecord = record
      this.fileVisiable = true
      if (record.fileId != undefined && record.fileId != '') {
        this.$refs.upFile.fetch(record.fileId)
      }
    },
    setFileId (fileId, fileUrl) {
      this.fileVisiable = false
      console.log(fileUrl)
      /**
       const dataSource = [...this.dataSource]
       console.log(this.editRecord.id)
       let record=dataSource.filter(p=>p.id===this.editRecord.id)
       console.log(record)*/
      this.editRecord["fileId"] = fileId
      this.editRecord["fileUrl"] = fileUrl
      //this.dataSource =[...dataSource]
    },
    onSelectChange (selectedRowKeys, selectedRows) {
      if (selectedRows.length > 0) {
        if (selectedRows[0].state != 3 && selectedRows[0].state != 1) {
          this.selectedRowKeys = selectedRowKeys
        }
      }
      else{
        this.selectedRowKeys = selectedRowKeys
      }
    },
    handleChange (date, dateStr, record, filedName) {
      const value = dateStr
      record[filedName] = value
    },
    inputChange (value, record, filedName) {
      record[filedName] = value
      if(filedName=='srPrizeRanknum'){
         record["auditName"] = this.calcFenShu(record['srProjectGrade'],record['srProjectLevel'],record['srPrizeRanknum'])
      }
    },
    onIsUseChange (e, record, filedName) {
      record[filedName] = e.target.checked;
    },
    handleSelectChange(v, f, record, filedName) {
      record[filedName] = v;
      record["auditName"] = this.calcFenShu(record['srProjectGrade'],record['srProjectLevel'],record['srPrizeRanknum'])
    },
    handleAdd () {
      for (let i = 0; i < 4; i++) {
        this.dataSource.push({
          id: (this.idNums + i + 1).toString(),
          state: 0,
          fileId: '',
          fileUrl: '',
          spProjectName: '',
          srProjectGrade: '',
          srProjectLevel: '',
          srPrizeDept: '',
          srPrizeDate: '',
          srPrizeRanknum: '',
          daoshiRanknum: '',
          auditName: '0',
          isUse: false
        })
      }
      this.idNums = this.idNums + 4
    },
    calcFenShu(jb,dj,rank){
      let fs='0';
       if (rank==1){
         if(jb=='国家级'|| jb=='省部级'){
           if(dj=='一等奖'){
            fs= '10'
           }
           if(dj=='二等奖'){
            fs= '5'
           }
           if(dj=='三等奖'){
            fs= '3'
           }
         }
       }
       return fs;
    },
    handleSave () {
      const dataSourceAll = [...this.dataSource]
      const dataSource = dataSourceAll.filter(p=>p.state==0 ||p.state==2)
      let dataAdd = []
      dataSource.forEach(element => {
        if (element.fileId != '' || element.fileUrl != '' || element.spProjectName != '' || element.srProjectGrade != '' || element.srProjectLevel != '' || element.srPrizeDept != '' || element.srPrizeDate != '' || element.srPrizeRanknum != '') {
          dataAdd.push(element)
        }
      });
      if (dataAdd.length < 0) {
        this.$message.warning('请填写数据！！！')
      }
      else {
        let jsonStr = JSON.stringify(dataAdd)
        this.loading = true
        this.$post('dcaBDocScientificprize/addNew', {
          jsonStr: jsonStr,
          state: 0
        }).then(() => {
          // this.reset()
          this.$message.success('保存成功')
          this.fetch()
          this.loading = false
        }).catch(() => {
          this.loading = false
        })
      }
    },
    handleSubmit () {
      let that = this
      this.$confirm({
        title: '确定提交全部记录?',
        content: '当您点击确定按钮后，信息将不能修改',
        centered: true,
        onOk () {
          const dataSourceAll = [...that.dataSource]
      const dataSource = dataSourceAll.filter(p=>p.state==0 ||p.state==2)
          let dataAdd = []
          dataSource.forEach(element => {
            if (element.fileId != '' || element.fileUrl != '' || element.spProjectName != '' || element.srProjectGrade != '' || element.srProjectLevel != '' || element.srPrizeDept != '' || element.srPrizeDate != '' || element.srPrizeRanknum != '') {
              dataAdd.push(element)
            }
          });
          if (dataAdd.length < 0) {
            that.$message.warning('请填写数据！！！')
          }
          else {
            let jsonStr = JSON.stringify(dataAdd)
            that.loading = true
            that.$post('dcaBDocScientificprize/addNew', {
              jsonStr: jsonStr,
              state: 1
            }).then(() => {
              //this.reset()
              that.$message.success('提交成功')
               that.fetch()
              that.CustomVisiable = false //提交之后 不能再修改
              that.loading = false
            }).catch(() => {
              that.loading = false
            })
          }
        },
        onCancel () {
          that.selectedRowKeys = []
        }
      })


    },
    handleDelete () {
      if (!this.selectedRowKeys.length) {
        this.$message.warning('请选择需要删除的记录')
        return
      }
      let that = this
      this.$confirm({
        title: '确定删除所选中的记录?',
        content: '当您点击确定按钮后，这些记录将会被彻底删除',
        centered: true,
        onOk () {
          let dcaBPatentIds = that.selectedRowKeys.join(',')
          const dataSource = [...that.dataSource];
          let new_dataSource = dataSource.filter(p => that.selectedRowKeys.indexOf(p.id) < 0)
          that.dataSource = new_dataSource
          that.$message.success('删除成功')
          that.selectedRowKeys = []
        },
        onCancel () {
          that.selectedRowKeys = []
        }
      })
    },
    fetch () {
      this.$get('dcaBDocScientificprize/custom', {
      }).then((r) => {
        let data = r.data
        this.dataSource = data.rows

        for (let i = 0; i < 4; i++) {
          this.dataSource.push({
            id: (this.idNums + i + 1).toString(),
            state: 0,
            fileId: '',
            fileUrl: '',
            spProjectName: '',
            srProjectGrade: '',
            srProjectLevel: '',
            srPrizeDept: '',
            srPrizeDate: '',
            srPrizeRanknum: '',
            daoshiRanknum: '',
            auditName: '0',
            isUse: false
          })
          this.idNums = this.idNums + 4
        }
      })
    }
  },
  computed: {
    columns () {
      return [
      {
        title: '获奖项目名称',
        dataIndex: 'spProjectName',
        width: 200,
        scopedSlots: { customRender: 'spProjectName' }
      },
      {
        title: '奖项级别',
        dataIndex: 'srProjectGrade',
        width: 130,
        scopedSlots: { customRender: 'srProjectGrade' }
      },
      {
        title: '奖项等级',
        dataIndex: 'srProjectLevel',
        width: 130,
        scopedSlots: { customRender: 'srProjectLevel' }
      },
      {
        title: '授奖部门',
        dataIndex: 'srPrizeDept',
        width: 130,
        scopedSlots: { customRender: 'srPrizeDept' }
      },
      {
        title: '批准年月',
        dataIndex: 'srPrizeDate',
        width: 130,
        scopedSlots: { customRender: 'srPrizeDate' }
      },
      {
        title: '本人排名',
        dataIndex: 'srPrizeRanknum',
        width: 130,
        scopedSlots: { customRender: 'srPrizeRanknum' }
      },
        {
        title: '合作导师排名',
        dataIndex: 'daoshiRanknum',
        width: 130,
        scopedSlots: { customRender: 'daoshiRanknum' }
      },
       {
        title: '分数',
        dataIndex: 'auditName',
        width: 80,
     
      },
      {
        title: '状态',
        dataIndex: 'state',
        width: 100,
        customRender: (text, row, index) => {
          switch (text) {
            case 0:
              return <a-tag color="purple">未提交</a-tag>
            case 1:
              return <a-tag color="green">已提交</a-tag>
            case 2:
              return <a-tag color="red">审核未通过</a-tag>
            case 3:
              return <a-tag color="#f50">已审核</a-tag>
            default:
              return text
          }
        }
      },
      {
        title: '审核意见',
        dataIndex: 'auditSuggestion'
      },
      
      {
        title: '附件',
        dataIndex: 'fileId',
        scopedSlots: { customRender: 'fileId' },
        width: 80
      }]
    }
  },
}
</script>

